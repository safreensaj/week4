//Symbol and BigInt
//Symbol
//Symbol is a unique and immutable value.
//Mostly used as object property keys.

//Example:

let id = Symbol("id");

//Even if description is same, symbols are always unique.


//BigInt
//BigInt is used to store very large numbers.

//Example:

let big = 12345678901234567890n;

//"Symbol is a unique primitive value used as object keys. 
// BigInt is used to store very large integers that exceed the normal number limit."

