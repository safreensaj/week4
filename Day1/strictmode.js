//Strict Mode – "use strict"

//How to Enable It
//We enable strict mode by writing "use strict"; at the top of a file or inside a function.

//Example:
//"use strict";

//Differences in Behaviour
//In strict mode:
//Cannot use undeclared variables
//Silent errors become real errors
//Duplicate parameter names are not allowed
//this in function is undefined (not window)

//Example:
//"use strict";
//x = 10; //  Error

//Strict mode makes code safer and cleaner.